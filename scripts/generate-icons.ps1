# Create dummy icon files for development
Write-Host "Creating icon placeholder files..."

# Create the icons directory if it doesn't exist
$iconsDir = ".\assets"
if (-not (Test-Path $iconsDir)) {
    New-Item -ItemType Directory -Path $iconsDir -Force
}

# Create the icon files
$iconSizes = @(16, 32, 48, 128)
$iconFiles = @()

foreach ($size in $iconSizes) {
    $normalIcon = Join-Path $iconsDir "icon$size.png"
    $disabledIcon = Join-Path $iconsDir "icon$size-disabled.png"
    
    # Create placeholder files (1x1 transparent PNG)
    [byte[]]$bytes = @(
        0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D,
        0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
        0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0x15, 0xC4, 0x89, 0x00, 0x00, 0x00,
        0x0B, 0x49, 0x44, 0x41, 0x54, 0x08, 0xD7, 0x63, 0x60, 0x00, 0x02, 0x00,
        0x00, 0x05, 0x00, 0x01, 0xE2, 0x26, 0x05, 0x9B, 0x00, 0x00, 0x00, 0x00,
        0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
    )
    
    [System.IO.File]::WriteAllBytes($normalIcon, $bytes)
    [System.IO.File]::WriteAllBytes($disabledIcon, $bytes)
    
    $iconFiles += $normalIcon
    $iconFiles += $disabledIcon
}

Write-Host "Created icon files:"
$iconFiles | ForEach-Object { Write-Host "  - $_" } 